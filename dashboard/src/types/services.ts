export const getServiceById = (id: string): Service | undefined => {
  return services.find((s) => s.id === id);
};

export const getResourceById = (
  serviceId: string,
  resourceId: string
): ServiceResource | undefined => {
  const service = getServiceById(serviceId);
  return service?.resources.find((r) => r.id === resourceId);
};

/**
 * Get API path for a resource
 */
export const getResourceApiPath = (serviceId: string, resourceId: string): string | undefined => {
  const resource = getResourceById(serviceId, resourceId);
  return resource?.path;
};
